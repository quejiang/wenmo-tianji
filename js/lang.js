  function toTraditional(text) {
    var result = '';
    for (var i = 0; i < text.length; i++) {
      var ch = text[i];
      result += S2T[ch] || ch;
    }
    return result;
  }

  function walkTextNodes(root, fn) {
    var walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, null, false);
    var node;
    var skipTags = { SCRIPT: 1, STYLE: 1, NOSCRIPT: 1, INPUT: 1, TEXTAREA: 1, SELECT: 1 };
    while ((node = walker.nextNode())) {
      if (node.parentNode && skipTags[node.parentNode.tagName]) continue;
      if (node.nodeValue.trim()) fn(node);
    }
  }

  function applyLanguage() {
    if (currentLang === 'zh-CN') return; // simplified is default, no conversion needed
    walkTextNodes(document.body, function(node) {
      node.nodeValue = toTraditional(node.nodeValue);
    });
  }

  function toggleLanguage() {
    currentLang = currentLang === 'zh-CN' ? 'zh-TW' : 'zh-CN';
    localStorage.setItem('wmtj_lang', currentLang);
    location.reload();
  }

