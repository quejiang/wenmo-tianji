  function calcGrids(name) {
    var chars = name.split('');
    var len = chars.length;
    var strokes = chars.map(getStrokes);
    var tian = 0, ren = 0, di = 0, wai = 0, zong = 0;

    if (len === 1) {
      tian = strokes[0] + 1;
      ren = strokes[0] + 1;
      di = strokes[0];
      wai = 1;
      zong = strokes[0];
    } else if (len === 2) {
      tian = strokes[0] + 1;
      ren = strokes[0] + strokes[1];
      di = strokes[1] + 1;
      wai = 2;
      zong = strokes[0] + strokes[1];
    } else {
      // >= 3字：姓+1=天, 姓+名2=人, 名2+名3=地, 名3+1=外, 总和=总
      tian = strokes[0] + 1;
      ren = strokes[0] + strokes[1];
      di = strokes[1] + strokes[len-1];
      wai = strokes[len-1] + 1;
      zong = strokes.reduce(function(a,b){return a+b;}, 0);
    }

    return [tian, ren, di, wai, zong, strokes];
  }

