  // 根据rank获取小阿卡纳名称
  function getRankName(rank) {
    if (rank === 1) return '王牌';
    if (rank === 11) return '侍从';
    if (rank === 12) return '骑士';
    if (rank === 13) return '王后';
    if (rank === 14) return '国王';
    var cn = ['零','一','二','三','四','五','六','七','八','九','十'];
    return cn[rank];
  }

  function drawTarot(count) {
    count = count || 3;
    var deck = MAJOR_ARCANA.concat(MINOR_ARCANA);
    // Fisher-Yates shuffle
    for (var i = deck.length - 1; i > 0; i--) {
      var j = Math.floor(Math.random() * (i + 1));
      var tmp = deck[i];
      deck[i] = deck[j];
      deck[j] = tmp;
    }
    var drawn = deck.slice(0, count);
    return drawn.map(function(c) {
      var reversed = Math.random() < 0.3;
      return {
        card: c,
        reversed: reversed,
        displayName: (c.rank ? (c.suit + getRankName(c.rank)) : c.name) + (reversed ? ' (逆位)' : ' (正位)'),
        desc: reversed ? '【逆位】' + (c.reversed || '') : '【正位】' + (c.upright || c.desc || '')
      };
    });
  }

