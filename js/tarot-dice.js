  function rollDice() {
    var planet = DICE_PLANETS[Math.floor(Math.random() * DICE_PLANETS.length)];
    var sign = DICE_SIGNS[Math.floor(Math.random() * DICE_SIGNS.length)];
    var house = DICE_HOUSES[Math.floor(Math.random() * DICE_HOUSES.length)];

    function buildInterpretation(p, s, h) {
      var interpretation = '';

      // 标题
      interpretation += p.glyph + ' ' + p.name + ' 落在 ' + s.symbol + ' ' + s.name + '，于' + h.name.replace(/\(.*\)/,'') + '领域展现。\n\n';

      // 行星在星座的表达
      interpretation += '【行星】' + p.name + '（' + p.short + '）\n';
      interpretation += '【星座】' + s.name + '，属' + s.mode + s.element + '象。' + s.meaning + '\n';
      interpretation += '【宫位】' + h.name + ' —— ' + h.meaning + '\n\n';

      // 综合解读
      interpretation += '📖 综合解读：\n';
      interpretation += '当代表' + p.short.split('、')[0] + '的' + p.name + '通过' + s.name + '（' + s.element + '象·' + s.mode + '模式）的表达方式，';
      interpretation += '在' + h.name.replace(/\(.*\)/,'') + '的领域展现它的能量时，';
      interpretation += '宇宙在告诉你：' + p.short.split('、')[0] + '是你此刻最需要关注的品质，';
      interpretation += '用' + s.keywords.split('、').slice(0,2).join('和') + '的方式来行动，';
      interpretation += '聚焦于' + h.name.replace(/\(.*\)/,'') + '所代表的' + h.meaning.substring(h.meaning.indexOf('——')+2, h.meaning.indexOf('。')).substring(0,8) + '等方面。\n\n';

      // 实用建议
      interpretation += '💡 给你的提示：\n';
      if (s.element === '火') interpretation += '火象的' + s.name + '给你的行动注入了热力和勇气。大胆去做，但不要冲动。';
      else if (s.element === '土') interpretation += '土象的' + s.name + '告诉你要脚踏实地。慢就是快，稳就是赢。';
      else if (s.element === '风') interpretation += '风象的' + s.name + '激活了你的思维和沟通。多交流、多学习、保持开放的心态。';
      else interpretation += '水象的' + s.name + '唤醒了你的情感和直觉。相信你的感受，它们是你最好的导航仪。';

      interpretation += '\n这颗骰子的核心信息是：' + p.short.split('、')[0] + '的品质 + ' + s.keywords.split('、')[0] + '的态度 + ' + h.name.replace(/\(.*\)/,'') + '的视角 = 你此刻的答案。';

      return interpretation;
    }

    return {
      planet: planet,
      sign: sign,
      house: house,
      interpretation: buildInterpretation(planet, sign, house)
    };
  }

